function isLegalProofEnabled() {
  return process.env.LEGAL_PROOF_DISABLED !== 'true';
}

function isLegalProofPlan(_plan) {
  return isLegalProofEnabled();
}

function requiresTsaOnStamp() {
  return isLegalProofEnabled() && process.env.TSA_OPTIONAL !== 'true';
}

function hasCreatorAttestation(stamp) {
  return !!(
    stamp.creatorAttestationAt &&
    stamp.creatorAttestationName &&
    stamp.creatorAttestationSignature &&
    stamp.creatorAttestationPayload
  );
}

function needsReattestation(stamp) {
  return !!(stamp.creatorAttestationAt && stamp.creatorAttestationName && !stamp.creatorAttestationSignature);
}

