/**
 * Legal proof configuration.
 * Default: development TSA (RFC 3161 via configured URL, typically FreeTSA).
 */

const path = require('path');
const fs = require('fs');
const { MARKETING, TSA_LABELS, LEGAL_COPY_VERSION } = require('../content/legalCopy');

const TSA_URL = process.env.TSA_URL || 'https://freetsa.org/tsr';
const TSA_MODE = process.env.TSA_MODE || 'development';
const TSA_PROVIDER_NAME = process.env.TSA_PROVIDER_NAME || 'RFC 3161 TSA (development)';

const DEFAULT_TSA_CA_PATH = path.join(__dirname, '../../certs/freetsa-cacert.pem');

function resolveTsaCaPath(configured) {
  if (!configured) {
    return fs.existsSync(DEFAULT_TSA_CA_PATH) ? DEFAULT_TSA_CA_PATH : '';
  }
  const resolved = path.isAbsolute(configured)
    ? configured
    : path.resolve(__dirname, '../..', configured);
  return fs.existsSync(resolved) ? resolved : configured;
}

const TSA_CA_CERT_PATH = resolveTsaCaPath(process.env.TSA_CA_CERT_PATH) ||
  (fs.existsSync(DEFAULT_TSA_CA_PATH) ? DEFAULT_TSA_CA_PATH : '');
const TSA_CA_CERT_PEM = process.env.TSA_CA_CERT_PEM || '';

const FREETSA_HOSTS = ['freetsa.org', 'www.freetsa.org'];

function isFreeTsaUrl(url) {
  try {
    const host = new URL(url).hostname.toLowerCase();
    return FREETSA_HOSTS.some((h) => host === h || host.endsWith(`.${h}`));
  } catch {
    return false;
  }
}

function resolveTsaTier() {
  if (TSA_MODE === 'production' && !isFreeTsaUrl(TSA_URL)) {
    return 'production';
  }
  return 'development';
}

function assertTsaAllowedForStamp() {
  const tier = resolveTsaTier();
  if (tier === 'production') return { tier, ok: true };

  if (TSA_MODE === 'production' && isFreeTsaUrl(TSA_URL) && process.env.TSA_ALLOW_FREE_IN_PROD !== 'true') {
    return {
      tier,
      ok: false,
      error: 'Production mode requires a commercial TSA URL. Set TSA_MODE=development to use development TSA, or TSA_ALLOW_FREE_IN_PROD=true.',
    };
  }
  return { tier, ok: true };
}

function getTsaDisplayMeta() {
  const tier = resolveTsaTier();
  return {
    tier,
    providerName: TSA_PROVIDER_NAME,
    url: TSA_URL,
    ...TSA_LABELS[tier],
  };
}

